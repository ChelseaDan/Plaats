var mongoose = require('mongoose');
var crypto = require('crypto');

module.exports = mongoose.model('User', {
  email: {
    type: String,
    unique: true,
    required: true
  },
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  hash: String,
  salt: String
});

